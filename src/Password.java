    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String sql="select * from atm_machine where accountno='"+accountno+"'";
            ps = (com.mysql.jdbc.PreparedStatement) conn.prepareStatement(sql);
            rs=ps.executeQuery();
            while(rs.next()){
                String password=rs.getString("password");
                String accountno=rs.getString("accountno");
                String value=jTextField1.getText();
                if(password.equals(value)){
                    String value1=jTextField2.getText();
                    String value2=jTextField3.getText();
                    if(value1.equals(value2)){
                        ps.execute("update atm_machine set password='"+value1+"' where accountno='"+accountno+"'");
                        JOptionPane.showMessageDialog(null, "Password changed Successfully");
                    }
                    else{
                        JOptionPane.showMessageDialog(null, "Password doesn't match");
                    }
                }
                else{
                        JOptionPane.showMessageDialog(null, "Old password doesn't match");
                }
                
            }
            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

